package com.taotao.cloud.java.javase.day02.Demo11;

public class TestOperation2{

	public static void main(String[] args){

		int a = 10; //赋值运算符


		a += 5; //在a基础上+5      a = a + 5;


		System.out.println(a);


		int b = 20;

		b -= 3;// b = b - 3;

		System.out.println(b);


		int c = 30;

		c %= 4;// c = c % 4;

		System.out.println(c);
	}
}
