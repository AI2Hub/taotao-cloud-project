package com.taotao.cloud.reactive.order.bean;

import lombok.Builder;
import lombok.Data;

/**
 * AdVo
 *
 * @author shuigedeng
 * @version 1.0.0
 * @since 2021/03/03 16:26
 */
public class AdVo {
	private String name;
	private String link;
	private String url;
	private String content;

	public AdVo(){}

	public AdVo(String name, String link, String url, String content) {
		this.name = name;
		this.link = link;
		this.url = url;
		this.content = content;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}
}
