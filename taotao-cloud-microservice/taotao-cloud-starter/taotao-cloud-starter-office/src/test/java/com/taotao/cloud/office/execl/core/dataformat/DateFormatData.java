package com.taotao.cloud.office.execl.core.dataformat;

public class DateFormatData {
    private String date;
    private String dateStringCn;
    private String dateStringUs;
    private String number;
    private String numberStringCn;
    private String numberStringUs;

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getDateStringCn() {
		return dateStringCn;
	}

	public void setDateStringCn(String dateStringCn) {
		this.dateStringCn = dateStringCn;
	}

	public String getDateStringUs() {
		return dateStringUs;
	}

	public void setDateStringUs(String dateStringUs) {
		this.dateStringUs = dateStringUs;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getNumberStringCn() {
		return numberStringCn;
	}

	public void setNumberStringCn(String numberStringCn) {
		this.numberStringCn = numberStringCn;
	}

	public String getNumberStringUs() {
		return numberStringUs;
	}

	public void setNumberStringUs(String numberStringUs) {
		this.numberStringUs = numberStringUs;
	}
}
